package java;

import java.util.ArrayList;

public class DynamicArray {
    public static void main(String args[]) {
        // The dynamic array for java is handled
        // through the ArrayList class
        // Takes anything descendant of object
        // non-primitives
        // Initial capacity can be specified
        var dynamicArray = new ArrayList<>();
        System.out.println(dynamicArray);
        // prints pointer/array location rather than data
    }
}
